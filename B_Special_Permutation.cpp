void run(){
    int t;
    cin>>t;
    while(t--){
        int n,a,b;
        cin>>n>>a>>b;
        int tep=n/2;
        vector<int> v;
        v.push_back(a);
        map<int,int> m;
        vector<int> u;
        m[a]=1;
        for(int i=n;i>=a;i--){
            if(i==b){
                continue;
            }
            if(v.size()==tep){
                break;
            }
            m[i]=1;
            v.push_back(i);
        };
        int largest=0;
        for(int i=1;i<=n;i++){
            if(m[i]!=1){
                u.push_back(i);
                largest=max(largest,u[u.size()-1]);
            }
        }
        if(largest==b && v.size()==tep && u.size()==tep){
            for(int a:v){
                cout<<a<<" ";
            }
            for(int a:u){
                cout<<a<<" ";
            }
            cout<<endl;
        }else{
            cout<<-1<<endl;
        }

        
    }
}

